#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned long	i;
	char			*temp;

	if (n > 0)
	{
		i = 0;
		temp = (char *)s;
		while (i < n)
		{
			*(temp + i) = 0;
			i++;
		}
	}
}
